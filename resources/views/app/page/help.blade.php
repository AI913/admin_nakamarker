    <h3>ヘルプ</h3>
    <div class="content-title">・登録マーカーとは何ですか？</div>
    <div class="content-body">
        登録マーカーとは位置情報を登録するためのマーカーです。
        マップ画面右下にある表示をタップして「とうろく」にして周りにあるマーカーをスワイプして設置したいマーカーを選択します。そのマーカーを地図上にドラッグ・アンド・ドロップして下さい。
        ※上手くマーカーを掴めない場合は、マーカーを少し長押ししてみて下さい。
    </div>
    <div class="content-title">・検索マーカーとは何ですか？</div>
    <div class="content-body">
        検索マーカーとは施設やランドマークを検索するためのマーカーです。
        マップ画面右下にある表示をタップして「けんさく」にして周りにあるマーカーをスワイプして設置したいマーカーを選択します。そのマーカーを地図上にドラッグ・アンド・ドロップして下さい。マーカーを設置した場所を中心に検索し、近い順に施設が表示されます。
    </div>
    <div class="content-title">・機能マーカーとは何ですか？</div>
    <div class="content-body">
        機能マーカーとは設置することで様々な機能が利用できるマーカーです。
        例）ドラッグ・アンド・ドロップした場所にナビゲーションしてくれる。
        マップ画面右下にある表示をタップして「きのう」にして周りにあるマーカーをスワイプして設置したいマーカーを選択します。
    </div>
    <div class="content-title">・マップに表示されている罫線は何ですか？</div>
    <div class="content-body">
        左上に１マスあたりの距離が表示されており、感覚的に距離が分かるようになっています。
        またマス目はタップできるようになっており、位置情報の登録等の場所に関するメニューを表示することも出来ます。
    </div>
    <div class="content-title">・コミュニティとは何ですか？</div>
    <div class="content-body">
        コミュニティに参加することでコミュニティ内で登録された位置情報を見ることが出来ます。
    </div>
